        <h3> What is an Enrolled Nurse? </h3>
        <p>
          An enrolled nurse works under the direction and supervision of a registered nurse in order to provide health care to patients in any clinical setting. Enrolled nurses provide physical and emotional care to more complex duties such as giving medications. Enrolled nurse will often have a chance to work in different clinical specialities including medical, surgical, mental health, paediatrics, orthopaedics, rehabilitation, oncology, operating theatres, rural and remote, emergency and community health care settings. Also, they may be found in aged care settings.
        </p>
        <h3> Duties </h3>
        <ul>
          <li>Observing and recording patients’ vital statistics and reporting the changes in patients’ conditions and the data to Registered Nurses or doctors who are in charge
          </li>
          <li>Providing physical and emotional support to patients and their families
          </li>
          <li>Assisting with activities of daily living such as personal hygiene, bathing, dressing and feeding
          </li>
        </ul>
        <h3> Special situation </h3>
        <p>
          Enrolled nurses act as a helper and co-worker for registered nurses. They may directly or indirectly be supervised by registered nurses depending on their nature of the work and the clinical environment they are assigned. Enrolled nurses can also provide support to patients in aged care, welfare, communities and community events.
        </p>
        <h3> Job requirements </h3>
        <img src="/img/become-en-2.webp" alt="" />
        <p>
          The minimum level of study required for an enrolled nurse in Australia is a Diploma of Nursing. It is currently open for people in society (not current students) who keep Certification III with relevant industry experience or a Certification IV or higher, too. Applicants should apply to the NMBA (Nursing and Midwifery Board of Australia) to work as an enrolled nurse in Australia. This certification should be renewed annually.
        </p>
        <h3> How to apply for an enrolled nurse as an international student </h3>
        <p>
          Students should complete the Diploma of Nursing first. A Diploma of Nursing is completed on a full-time basis and can be completed in 20 months. The courses are also completed face to face on campus and contain 400 hours of unpaid clinical placement in a range of professional workplace settings. After achieving a Diploma of Nursing, appliers can apply to become an Enrolled Nurse. This program is not based on the ATAR (Australian Tertiary Admissions Rank) but the requirements are set by the course providers. For more details please visit:
          <a href="http://www.nursingmidwiferyboard.gov.au/">http://www.nursingmidwiferyboard.gov.au/</a>
        </p>
        <h3>Study to become an enrolled nurse in Australia as an international student</h3>
        <p>
          The minimum requirement for an enrolled nurse is a diploma. In order to achieve a diploma, international students need to commit to linguistic accuracy when using clinical terminology. They must also commit to achieving high levels of English in order to be accepted as a nurse and to function well in the nursing and nursing study environment. After completing a Diploma of Nursing, students may either stay as an enrolled nurse for their career or combine working as an enrolled nurse and pursue further study to apply for a Bachelor’s degree in Nursing. This will help progress their skills and knowledge to become future registered nurse.
        </p>
        <h3>Challenges</h3>
        <p>
          Enrolled nurses must be prepared to be supervised and report to registered nurses. Therefore respect for authority is important. Enrolled nurses should also have a clear and possible plan for the future careers and understand that they will need to regularly update their skills and knowledge in order to stay relevant and update to nursing trends, nursing environment expectations and to ensure they provision quality nursing care.
        </p>
        <h3> Salary </h3>
        <p>
          The most common annual salary in Australia for an enrolled nurse is between 55k AUD to 65k AUD.
        </p>
